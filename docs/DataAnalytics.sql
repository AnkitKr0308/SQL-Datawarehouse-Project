--DATE EXPLORATION: Identify the earliest and latest dates (boundaries) to understand the scope of date and timespan.

SELECT 
	MIN(order_date) AS first_order_date,
	MAX(order_date) AS last_order_date,
	DATEDIFF(MONTH, MIN(order_date), MAX(order_date) ) AS order_range
FROM gold.fact_Sales

SELECT 
	MIN(birth_date) AS oldest_customer,
	DATEDIFF(YEAR, MIN(birth_date), GETDATE() ) AS oldest_age,
	MAX(birth_date) AS  youngest_customer,
	DATEDIFF(YEAR, MAX(birth_date), GETDATE() ) AS youngest_age
FROM gold.dim_customers

