--MEASURES EXPLORATION
--Calculate the key metric of business 
--Highest Level of Aggregation | Lowest Level of Details

--Find Total Sales
SELECT SUM (sales_amount) AS total_sales FROM gold.fact_Sales 

--Find how many items are sold
SELECT SUM (quantity) AS total_quantity FROM gold.fact_Sales

--Find the average selling price
SELECT AVG(price) AS avg_price FROM gold.fact_Sales

--Find the total number of orders
SELECT COUNT (order_number) AS total_orders FROM gold.fact_Sales
SELECT COUNT (DISTINCT order_number) AS total_orders FROM gold.fact_Sales -- Find unique orders

--Find total number of Products
SELECT COUNT (product_key) AS total_products FROM gold.dim_Products

--Find total number of Customers
SELECT COUNT (customer_key) AS total_customers FROM gold.dim_Customers

--Find total number of Customers that has placed an order
SELECT COUNT(DISTINCT customer_key) AS toal_customers FROM gold.fact_Sales

--Generate a report that shows all key metrics of business
SELECT 'Total Sales' AS measure_name, SUM (sales_amount) AS measure_value FROM gold.fact_Sales
UNION ALL
SELECT 'Total Quantity', SUM (quantity) FROM gold.fact_Sales
UNION ALL
SELECT 'Average Selling Price', AVG(price) FROM gold.fact_Sales
UNION ALL
SELECT 'Total Number of Orders', COUNT (DISTINCT order_number) FROM gold.fact_Sales
UNION ALL
SELECT 'Total Number of Products', COUNT (product_key)  FROM gold.dim_Products
UNION ALL
SELECT 'Total Number of Customers', COUNT (customer_key) FROM gold.dim_Customers
UNION ALL
SELECT 'Total Number of Customers who placed an order', COUNT(DISTINCT customer_key) FROM gold.fact_Sales

