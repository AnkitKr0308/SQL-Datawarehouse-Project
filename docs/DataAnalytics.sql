--RANKING: Orders the values of dimensions by measures

--5 products that generated highest revenue
SELECT TOP 5
	p.product_name,
	SUM (s.sales_amount) AS total_revenue
FROM gold.fact_Sales s
LEFT JOIN gold.dim_Products p
	ON p.product_key = s.product_key
GROUP BY p.product_name
ORDER BY total_revenue DESC

--The above query can also be written using windows function which provides more flexibility
SELECT * 
FROM (
	SELECT
		p.product_name,
		SUM (s.sales_amount) AS total_revenue,
		ROW_NUMBER() OVER (ORDER BY SUM (s.sales_amount) DESC) AS rank_prd
	FROM gold.fact_Sales s
	LEFT JOIN gold.dim_Products p
		ON p.product_key = s.product_key
	GROUP BY p.product_name
)t
WHERE rank_prd <=5

--5 worst performing products in terms of sales
SELECT TOP 5
	p.product_name,
	SUM (s.sales_amount) AS total_revenue
FROM gold.fact_Sales s
LEFT JOIN gold.dim_Products p
	ON p.product_key = s.product_key
GROUP BY p.product_name
ORDER BY total_revenue ASC

